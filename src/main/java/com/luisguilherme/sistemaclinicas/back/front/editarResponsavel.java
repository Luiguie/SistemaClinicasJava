    public void loadInfo(){
        idField.setText(Long.toString(resp.getIdResponsavel()));
        nomeField.setText(resp.getNomeResponsavel());
        emailField.setText(resp.getEmail());
        celularField.setText(resp.getCelular());
        telefoneField.setText(resp.getTelefone());
    }
    
    public boolean validar(){
        //valida todos os campos presentes no Jpanel e retorna falso caso algum
        //esteja invalido
        Validador v = new Validador();
        if(!v.validarCampo(nomeField)){
            JOptionPane.showMessageDialog(null, "Nome Invalido");
            return false;
        }
        if(!v.validarCampo(telefoneField)){
            JOptionPane.showMessageDialog(null, "Telefone Invalido");
            return false;
        }
        if(!v.validarCampo(celularField)){
            JOptionPane.showMessageDialog(null, "Celular Invalido");
            return false;
        }
        if(!v.validarEmail(emailField)){
            JOptionPane.showMessageDialog(null, "Email Invalido");
            return false;
        }
        
       return true;
    }
    
    public boolean editarInfo(){
        //checo se s√£o validos e populo
        if(!validar()){
            JOptionPane.showMessageDialog(null, "Campo(s) Invalido(s)");
            return false;
        }
        
        resp.setNomeResponsavel(nomeField.getText());
        resp.setTelefone(telefoneField.getText());
        resp.setCelular(celularField.getText());
        resp.setEmail(emailField.getText());
        
        return true;
    }

